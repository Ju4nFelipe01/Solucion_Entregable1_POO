package ejercicio1;

public class AsignarEdades {

    public double Alberto(double EdadJuan){
        return (EdadJuan*2)/3;
    }
    public double Ana(double EdadJuan){
        return (EdadJuan*4)/3;
    }
    public double Mama(double EdadJuan, double EdadAlberto, double EdadAna){
        return EdadAlberto+EdadJuan+EdadAna;
    }

}
